#addition
summe = 10 + 5
print(summe) # 15

#subtraktion
differenz = 10 - 5
print(differenz) # 5

#multiplikation
produkt = 10 * 5
print(produkt) # 50

#division
quotient = 10 / 5
print(quotient) # 2.0  (Quotienten werden immer als float ausgegeben)

#rest = modulus
rest = 10 % 3 # rechnet 10 / 3 und das Ergebnis ist der Rest
print(rest) # 1

kombination = (10 + 2) * (2 - 1)
print(kombination)

